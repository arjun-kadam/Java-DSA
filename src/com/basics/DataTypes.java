package com.basics;

public class DataTypes {
    public static void main(String[] args) {
        System.out.println("This Are Data Types");
        int i=10;
        float f=52.36f;
        char c='C';
        double d=1234.4343545;

        System.out.println("This is Integer: " + i );
        System.out.println("This is Float: " + f );
        System.out.println("This is Character: " + c );
        System.out.println("This is Double: " + d );
    }
}
